"""Системные промпты и шаблоны для LLM-агента (OpenRouter / minimax-m3).

ЦЕЛЬ агента задаёт всё его поведение: на основе цели и контекста (предыдущих
ответов) он формирует голосовые вопросы и в конце собирает чеклист."""

GOAL = (
    "Провести голосовое интервью с клиентом и заполнить чеклист созвона: "
    "название и суть проекта, цели и ключевые метрики, желаемые сроки, бюджет, "
    "технические требования и интеграции, а также риски и открытые вопросы."
)

# --- Генерация вопросов раунда ---------------------------------------------

QUESTIONS_SYSTEM = (
    "Ты — внимательный AI-ассистент, который проводит голосовое интервью с клиентом, "
    "чтобы заполнить чеклист созвона. Ты задаёшь короткие, понятные вопросы на русском "
    "языке, на которые удобно ответить голосом (1–2 предложения). Вопросы открытые, но "
    "конкретные, без вложенных подпунктов и без нумерации внутри вопроса. "
    "Отвечай СТРОГО валидным JSON без пояснений и без markdown-ограждений."
)


def questions_user(goal: str, round_number: int, n: int, history_text: str) -> str:
    if round_number == 1:
        ctx = "Это первый раунд. Задай вводные вопросы, чтобы понять проект в целом."
    else:
        ctx = (
            f"Это раунд {round_number}. Ниже — ответы клиента из предыдущих раундов. "
            "Проанализируй их и задай УТОЧНЯЮЩИЕ вопросы по темам чеклиста, которые ещё "
            "не раскрыты или раскрыты неполно. Не повторяй уже заданные вопросы."
        )
    return f"""ЦЕЛЬ ИНТЕРВЬЮ:
{goal}

КОНТЕКСТ:
{ctx}

ОТВЕТЫ КЛИЕНТА (предыдущие раунды):
{history_text or "— пока нет —"}

ЗАДАЧА:
Сформируй ровно {n} вопроса(ов) для этого раунда.
Верни JSON строго такого вида:
{{"summary": "одно короткое предложение-резюме предыдущих ответов (пустая строка для первого раунда)", "questions": ["вопрос 1", "вопрос 2", "вопрос 3"]}}"""


# --- Генерация итогового чеклиста ------------------------------------------

CHECKLIST_SYSTEM = (
    "Ты — AI-ассистент, который по итогам голосового интервью с клиентом составляет "
    "структурированный чеклист созвона. Ты анализируешь транскрипты ответов и заполняешь "
    "пункты, опираясь ТОЛЬКО на сказанное клиентом (ничего не выдумываешь). "
    "Отвечай СТРОГО валидным JSON без пояснений и без markdown-ограждений."
)


def checklist_user(goal: str, history_text: str) -> str:
    return f"""ЦЕЛЬ ИНТЕРВЬЮ:
{goal}

ВСЕ ОТВЕТЫ КЛИЕНТА (транскрипты):
{history_text}

ЗАДАЧА:
Составь чеклист созвона. Сгруппируй пункты по категориям:
"Общая информация", "Цели и задачи", "Сроки и бюджет", "Технические требования", "Риски и заметки".
Для каждого пункта определи статус:
- "confirmed" — информация чётко получена из ответов клиента;
- "needs_clarification" — тема затронута, но требует уточнения;
- "not_discussed" — важная тема, но клиент её не раскрыл.
Верни JSON строго такого вида:
{{"summary": "2–3 предложения общего резюме созвона",
  "items": [
    {{"category": "Общая информация", "item": "Название проекта: ...", "status": "confirmed", "notes": null}}
  ]}}
Пиши конкретику из ответов клиента. Если факта нет — ставь статус "not_discussed"."""
